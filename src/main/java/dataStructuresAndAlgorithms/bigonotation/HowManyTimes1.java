package dataStructuresAndAlgorithms.bigonotation;

/**
 * Find out how many times i<10 gets executed
 * @author srapolu
 *
 */
public class HowManyTimes1 {

	public static void main(String[] args) {
		int iterations =0;
		for(int i=0; i<10; i++) {
			iterations ++;
		}
		System.out.println(iterations);
	}

}
